// assignment and operators

let x = 5

//addition assignment

x += 1 // x = x+1

//subtraction assignment
x -= 1; // x = x-1

// multiplication assignment
x *= 1; //x = x*1

// division assignment
x /= 1; // x = x/1

//remainder/ mod

x %= 1;  //x = x % 1 or Xmod1

// exponential assignment
x **= 1 // x = x**1